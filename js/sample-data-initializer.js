    /**
     * Store data with multiple key formats for compatibility
     */
    async storeData(dataType, data) {
        const keys = this.getStorageKeys(dataType);
        
        for (const key of keys) {
            try {
                localStorage.setItem(key, JSON.stringify(data));
            } catch (error) {
            }
        }
    }
    
    /**
     * Get all possible storage keys for a data type
     */
    getStorageKeys(dataType) {
        const keyMappings = {
            products: ['products', 'fallback_products', 'firebaseProductData'],
            clients: ['clients', 'clientData', 'cloudClients', 'fallback_clients'],
            salespeople: ['salespeople', 'cloudSalesmen', 'fallback_salespeople'],
            colors: ['colors', 'fallback_colors'],
            styles: ['styles', 'fallback_styles'],
            categories: ['categories', 'fallback_categories'],
            priceLists: ['priceLists', 'fallback_price_lists']
        };
        
        return keyMappings[dataType] || [dataType];
    }
    
    /**
     * Check if data already exists in localStorage
     */
    hasExistingData(dataType) {
        const keys = this.getStorageKeys(dataType);
        
        for (const key of keys) {
            const data = localStorage.getItem(key);
            if (data) {
                try {
                    const parsed = JSON.parse(data);
                    if (Array.isArray(parsed) && parsed.length > 0) {
                        return true;
                    }
                } catch (error) {
                    // Ignore parsing errors
                }
            }
        }
        
        return false;
    }
    
